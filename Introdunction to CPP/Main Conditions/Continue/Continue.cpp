#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
    {
        cout << "hi" << endl;
        cout << "hello" << endl;

        continue; // continue is used to continue the iteration

        cout << "Namaste  sbhi ko" << endl;
    }

    return 0;
}
